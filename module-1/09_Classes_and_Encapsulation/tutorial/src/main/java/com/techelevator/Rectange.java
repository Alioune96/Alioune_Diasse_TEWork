package com.techelevator;

public class Rectange {
    private int width;
    private int lenght;

    private int area;

    public void setWidth(int width) {
        this.width = width;
    }

    public void setLenght(int lenght) {
        this.lenght = lenght;
    }

    public void setArea(int area) {
        this.area = area;
    }

    public Rectange(int width) {
        this.width = width;
    }

    public int getWidth() {
        return width;
    }

    public int getLenght() {
        return lenght;
    }

    public int getArea() {
        return area;
    }

}
